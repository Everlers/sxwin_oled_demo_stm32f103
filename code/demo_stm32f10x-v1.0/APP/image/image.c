//风速64*32
u8 *windSpeed (u8 width,u8 high,u8 depth,u8 background,u8 winds)
{
	u8 *buf = Malloc(width * high);
	memset(buf,background,width * high);
	if(winds > 5)winds = 5;
	for(u8 i=0;i<winds;i++)
		oledFillToBuff(buf,64,32,i*12,(i*12)+10,((5-i)*5),31,depth);
	return buf;
}

//时间显示 单位：分钟
u8 *imageTime(u8 width,u8 high,u8 depth,u8 background,u16 time)
{
	u8 *buf = Malloc(width * high);
	memset(buf,background,width * high);
	if(time >= 60)//小时
	{
		time /= 60;
		oledShowGb16x16ToBuff(56,16,"时",depth,background,buf,width,high);
	}
	else//分钟
		oledShowGb16x16ToBuff(56,16,"分",depth,background,buf,width,high);
	if(time >= 100)time = 99;
	if(time/10)
		oledWriteNumber24x48ToBuff(0,0,time/10,depth,background,buf,width,high);
	oledWriteNumber24x48ToBuff(24,0,time%10,depth,background,buf,width,high);
	return buf;
}

//温度显示 72*48
u8 *imageTemp (u8 width,u8 high,u8 depth,u8 background,int temp)
{
	u8 *buf = Malloc(width * high);
	memset(buf,background,width * high);
	if(temp >= 100)temp = 99;
	if(temp <= 0)temp = 0;
	if(temp/10)
		oledWriteNumber24x48ToBuff(0,0,temp/10,depth,background,buf,width,high);
	oledWriteNumber24x48ToBuff(24,0,temp%10,depth,background,buf,width,high);
	for(int y=0;y<24;y++)
	{
		for(int x=0;x<3;x++)
		{
			for(int z=0;z<8;z++)
			{
				if(gimage_temp[(y * 3) + x] & (1<<z))
					buf[(y * width) + ((x * 8) + z + 48)] = depth;
				else
					buf[(y * width) + ((x * 8) + z + 48)] = background;
			}
		}
	}
	return buf;
}

u8 *imageTempLabel (u8 width,u8 high,u8 depth,u8 background,char *font)
{
	if(font == NULL)
		font = "没有标签";
	if(font[8] != 0)font[8] = 0;
	u8 *buf = Malloc(width * high);//imageGet(gimage_72x16FontFrame,sizeof(gimage_72x16FontFrame),depth,background);//提取背景框
	memset(buf,background,width * high);
	oledShowGb16x16ToBuff(0,0,font,depth,background,buf,width,high);
	return buf;
}

//图像转换(将图片转换成每个像素一个字节)
u8 *imageGet(const u8 *image,u16 size,u8 depth,u8 background)
{
	u8 *imageBuf = Malloc(size * 8);//分配内存
	for(u16 i=0;i<size;i++)//解析图片
	{
		for(u8 x=0;x<8;x++)
			imageBuf[x + (i * 8)] = (image[i] & BV(x)) ? depth:background;
	}
	return imageBuf;
}

u8 *image16GrayGet(const u8 *image,u32 size)
{
	u8 *imageBuf = Malloc(size * 2);//分配内存
	for(u32 i=0;i<size*2;i++)
		imageBuf[i] = (i%2) ? image[i/2]>>4 : image[i/2]&0x0F;
	return imageBuf;
}

//释放转换出来的图像空间
void imageFree(u8 *image)
{
	Free(image);
}

